		</p>

		<p class="lead">
		Just because you can kind of afford to build out your idea, doesn't mean you should pay for it, because chances are you're going to have to pivot your Company into a different direction, and when you've used up a lot of your money, you're not going to be willing to put more to it. It's best to do it yourself first even though it's not going to be amazing, and when you know your idea works - that's when you spend money. It sounds like a no brainer, and I wish it was back when I started this venture.
