		<p class="lead">
		<strong>What I did right: </strong>I got the site a lot of press. It was featured on <a href="http://www.webpronews.com/both-bloggers-and-advertisers-win-with-new-crowd-sourcing-platform-2011-09">Web Pro News</a>, <a href="http://www.blogworld.com/2011/08/29/bloggerscompete-com-bloggers-compete-advertisers-win/">Blog World</a>, <a href="http://www.blogengage.com/blogger/bloggers-compete-compete-with-other-bloggers/">Blog Engage</a> and <a href="http://weblogbetter.com/2011/08/01/promote-blog-contest/">We Blog Better</a>. I was able to launch the site much quicker than my first failure (3 times faster), but it still took over a year to launch the site. After all the SEO and press generation I did for the site it constantly gets organic traffic even though it's not active at all. It has over 350+ users on the site. The site also got paid customers. I sold the website on Flippa.com to Noizu Inc in Washington.
		</p>

		<p class="lead">
		<strong>What I did wrong:</strong> I hired a US freelancer instead of doing all the development myself. The freelancer I hired was late a lot of times and would code without testing the code. I was the one left testing and writing up bug reports. We didn't use version control, so we wrote over each other's work sometimes. I cut the features down significantly to the bare essentials, but we could've coded out things so that it could've been per client basis and launched in a month or so. I also hired a designer that made a lot of mistakes too. I had to write up docs with screen shots explaining what was wrong a lot. All in all, I spent a lot of money (close to 10 grand) and the site got customers, press, users, but in the end the idea didn't work out.
